// 100-operations.c

// Function: add
// Description: Takes two integers and returns their sum.
int add(int a, int b) {
    return a + b;
}

// Function: sub
// Description: Takes two integers and returns their difference.
int sub(int a, int b) {
    return a - b;
}

// Function: mul
// Description: Takes two integers and returns their product.
int mul(int a, int b) {
    return a * b;
}

// Function: div
// Description: Takes two integers and returns the integer division result.
int div(int a, int b) {
    return a / b;
}

// Function: mod
// Description: Takes two integers and returns the remainder of their division.
int mod(int a, int b) {
    return a % b;
}
